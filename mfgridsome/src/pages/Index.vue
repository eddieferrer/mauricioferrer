<script>

export default {
  components: {
  },
  metaInfo: {
    title: 'MAURICIO FERRER - RA, LEED AP'
  },
  mounted () {
  },
  computed: {
      sortedEdges() {
        function compare( a, b ) {
            if ( a.node.menu_order < b.node.menu_order ){
                return -1;
            }
            if ( a.node.menu_order > b.node.menu_order ){
                return 1;
            }
            return 0;
        }
        return this.$page.projects.edges.sort(compare);
      }
  },
}
